// Map icon name strings to components
function getIconComponent(iconName?: string) {
  if (!iconName) return null

  const name = iconName.toLowerCase()

  // Shield/Security icons
  if (name.includes('shield') || name.includes('security') || name.includes('secure') || name.includes('kalite')) {
    return <ShieldIcon />
  }

  // Speed/Flash icons
  if (name.includes('bolt') || name.includes('flash') || name.includes('zap') || name.includes('fast') || name.includes('hız') || name.includes('teslim')) {
    return <BoltIcon />
  }

  // Growth/Chart icons
  if (name.includes('chart') || name.includes('trend') || name.includes('graph') || name.includes('growth') || name.includes('büyüme')) {
    return <ChartIcon />
  }

  // Settings/Gear icons
  if (name.includes('cog') || name.includes('gear') || name.includes('settings') || name.includes('altyapı') || name.includes('cpu')) {
    return <CogIcon />
  }

  // Block icons
  if (name.includes('block') || name.includes('cube')) {
    return <BlocksIcon />
  }

  // Star/Zap for performance
  if (name.includes('star') || name.includes('performance')) {
    return <ZapIcon />
  }

  return <DefaultIcon />
}

// Color variants for cards - cycles through different tinted backgrounds
// Each variant includes explicit hover class (50 -> 100) to prevent global override
const cardVariants = [
  { bg: 'bg-primary-50 hover:bg-primary-100', avatar: 'bg-primary-100', text: 'text-primary-600' },
  { bg: 'bg-purple-50 hover:bg-purple-100', avatar: 'bg-purple-100', text: 'text-purple-600' },
  { bg: 'bg-teal-50 hover:bg-teal-100', avatar: 'bg-teal-100', text: 'text-teal-600' },
  { bg: 'bg-amber-50 hover:bg-amber-100', avatar: 'bg-amber-100', text: 'text-amber-600' },
]

export function FeaturesSection({ data }: Props) {
  return (
    <section className="section-features py-16 sm:py-24">
      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
        <div className="text-center">
          <h2 className="text-3xl font-bold tracking-tight sm:text-4xl">
            {data.title}
          </h2>
        </div>
        {/* Responsive grid: 1 col sm, 2 cols md, 3 cols lg - equal height */}
        <div className="mt-12 grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
          {data.items.map((item, index) => {
            const variant = cardVariants[index % cardVariants.length]
            const iconComponent = getIconComponent(item.icon)

            return (
              <div key={index} className="flex">
                <div className={`feature-card-v2 ${variant.bg} flex-1`}>
                  {/* Header: Title left, Icon right */}
                  <div className="flex items-center justify-between mb-3">
                    <h3 className="text-lg font-semibold text-gray-900 mb-0">{item.title}</h3>
                    {iconComponent && (
                      <span className={`flex-shrink-0 ml-3 w-11 h-11 rounded-full ${variant.avatar} ${variant.text} flex items-center justify-center`}>
                        {iconComponent}
                      </span>
                    )}
                  </div>
                  {/* Description */}
                  {item.description && (
                    <p className="text-gray-600 text-sm leading-relaxed mb-0">{item.description}</p>
                  )}
                </div>
              </div>
            )
          })}
        </div>
      </div>
    </section>
  )
}
